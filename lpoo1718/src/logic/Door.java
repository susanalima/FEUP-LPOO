package logic;

public class Door {
	private int x;
	private int y;
	
	/*******************CONSTRUCTORS*******************/
	
	public Door(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
	/*******************GET FUNCTIONS*******************/
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	/*******************SET FUNCTIONS*******************/
}
